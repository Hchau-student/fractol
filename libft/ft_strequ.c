#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	size_t		a;

	if (!s1 && !s2)
		return (1);
	if (!s1 || !s2)
		return (0);
	a = ft_strcmp(s1, s2);
	if (a != 0)
		return (0);
	return (1);
}

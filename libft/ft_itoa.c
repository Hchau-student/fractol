#include "libft.h"

char	*ft_itoa(int n)
{
	char		*res;
	size_t		i;
	long		a;

	a = n;
	i = 1;
	if (n < 0)
		i++;
	while ((n = n / 10) != 0 && (i + 1) > 0)
		i++;
	if (!(res = ft_strnew(i)))
		return (NULL);
	if (a == 0)
		res[0] = '0';
	if (a < 0)
	{
		res[0] = '-';
		a = a * -1;
	}
	while (a != 0)
	{
		res[--i] = a % 10 + 48;
		a = a / 10;
	}
	return (res);
}

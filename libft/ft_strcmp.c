#include "libft.h"

int		ft_strcmp(char const *s1, char const *s2)
{
	size_t		a;

	a = 0;
	while (s2[a] != '\0' && s1[a] != '\0' && (a + 1) > 0)
	{
		if (s1[a] == s2[a])
			a++;
		else
			break ;
	}
	return ((unsigned char)s1[a] - (unsigned char)s2[a]);
}

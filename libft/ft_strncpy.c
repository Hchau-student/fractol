#include "libft.h"

char	*ft_strncpy(char *dest, char const *src, size_t n)
{
	size_t	a;

	a = 0;
	while (a < n && src[a])
	{
		dest[a] = src[a];
		a++;
	}
	while (a < n)
	{
		dest[a] = '\0';
		a++;
	}
	return (dest);
}

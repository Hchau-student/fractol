#include "libft.h"

char	*ft_strchr(char const *s, int c)
{
	char	*my_s;
	size_t	i;

	my_s = (char *)s;
	i = 0;
	while (my_s[i] != '\0' && my_s[i] != c)
		i++;
	if (my_s[i] == '\0' && c != '\0')
		return (0);
	return (my_s + i);
}
